        //
        $page                   =   Page::where('slug','term')->first();
            
        $terms                  =   (object) array();
        $terms->text            =   $page->section()->where('meta_key','text')->value('meta_value');
        $terms->title           =   $page->section()->where('meta_key','pagetitle')->value('meta_value');
        $terms->subtitle        =   $page->section()->where('meta_key','pagesubtitle')->value('meta_value');

        return view('frontend.terms.index',compact('terms'));
    }

